-- =====================================================================
-- Hridaya Creations — PostgreSQL schema (reference DDL)
-- Mirrors the JPA entity model. Hibernate can also generate this
-- automatically via JPA_DDL_AUTO; this script is for explicit/managed
-- schema deployments. Safe to run on an empty database.
